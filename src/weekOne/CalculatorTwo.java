package weekOne;

public class CalculatorTwo {
    // 정수
    private int One;
    private int Two;

    public CalculatorTwo(int one, int two) {
        this.One = one;
        this.Two = two;
    }

    public int getOne() {
        return One;
    }

    public void setOne(int one) {
        One = one;
    }

    public int getTwo() {
        return Two;
    }

    public void setTwo(int two) {
        Two = two;
    }
}
